using DIGOS.Ambassador.Core.Database.Design;
using DIGOS.Ambassador.Core.Database.Services;
using DIGOS.Ambassador.Core.Services;
using DIGOS.Ambassador.Plugins.Moderation.Model;
using JetBrains.Annotations;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Design;
using Remora.EntityFrameworkCore.Modular.Services;

[assembly: DesignTimeServicesReference("DIGOS.Ambassador.Core.Database.Design." + nameof(AutoGeneratedDesignTimeServices) + ", DIGOS.Ambassador.Core.Database")]

namespace DIGOS.Ambassador.Plugins.Moderation.Design
{
    /// <summary>
    /// Design-time factory for <see cref="ModerationDatabaseContext"/> instances.
    /// </summary>
    [UsedImplicitly]
    public class DesignTimeModerationDatabaseContextFactory : IDesignTimeDbContextFactory<ModerationDatabaseContext>
    {
        /// <inheritdoc />
        public ModerationDatabaseContext CreateDbContext(string[] args)
        {
            var optionsBuilder = new DbContextOptionsBuilder<ModerationDatabaseContext>();
            new ContextConfigurationService
            (
                new ContentService(FileSystemFactory.CreateContentFileSystem()),
                new SchemaAwareDbContextService()
            )
            .ConfigureSchemaAwareContext<ModerationDatabaseContext>(optionsBuilder);

            return new ModerationDatabaseContext(optionsBuilder.Options);
        }
    }
}
